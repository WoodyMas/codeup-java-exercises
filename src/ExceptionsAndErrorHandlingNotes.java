    // What are Java exceptions?
    // Exceptions are a feature of the Java language to provide a formalized way to do error handling. They generally contain a message that describes what went wrong. When an application can't perform an operation, we call this a thrown exception.

    // What does it mean to throw an exception?
    // An exception is thrown when the application can't perform an operation
    //
    // What is the difference between exceptions and errors?
    // The error implies a problem that mostly arises due to the shortage of system resources. On the other hand, the exceptions occur during runtime and compile time.


    // What is the difference between checked and unchecked exceptions?
    // A checked exception is caught at compile time whereas a runtime or unchecked exception is, as it states, at runtime.

    // Is a RuntimeException checked or unchecked?
    // Run-time exception is called unchecked exception since it's not checked during compile time.

